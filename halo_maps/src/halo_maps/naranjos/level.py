"""Naranjos Halo CE level — assembly and JMS export.

This script is the final step in the Naranjos map pipeline.  It takes the
building geometry generated by ``generate_naranjos_dsl()`` and wraps it in a
**sealed world volume** (sky envelope) to produce a single valid ``bsp_world``
object ready for Halo CE's ``tool.exe`` BSP compiler.

Pipeline overview (from the halo_maps README)
---------------------------------------------
1. ``setup_scene()``                — metric units, standard collections
2. ``generate_naranjos_dsl()``      — create all building objects
   *(these two steps run in generate_dsl.py; bsp_merged is their output)*
3. ``assemble_naranjos_level()``    — **THIS FILE**
   a. Load ``bsp_merged`` into a bmesh and assign ``naranjos_wall`` material
   b. Remove any exact duplicate faces (safety pass against coplanar overlaps)
   c. Fill all open boundary edges with ``+portal`` faces to seal the buildings
   d. Add sky envelope geometry — 6-face inverted box (``+sky`` material)
   e. Triangulate the whole bmesh
   f. Write the combined bmesh to the new ``bsp_world`` object
4. ``validate_and_report()``        — sealed / manifold / normals checks
5. ``export_level()``               — write ``naranjos.jms``

Materials used
--------------
``naranjos_wall``  (slot 0)
    All building geometry (walls, slabs, campus paving) inherited from
    ``bsp_merged``.

``+portal``  (slot 1)
    Faces added to fill every door (and window) opening in the building
    walls.  Halo CE renders portal faces as invisible and passable, which
    keeps the BSP sealed while preserving the door holes.

``+sky``  (slot 2)
    All six faces of the sky envelope.  Halo CE renders these as open sky
    rather than a solid surface, but they must exist to seal the BSP.

Design notes
------------
* **No separate ground plane** — ``bsp_merged`` already contains the campus
  paving slab produced by ``generate_naranjos_dsl()``.  Adding a redundant
  z = 0 ground plane would create coplanar-overlap validation failures.

* **Two disconnected sealed volumes** — The sky envelope is a standalone
  6-face closed box that does *not* share edges with the building mesh.
  Halo CE's ``tool.exe`` BSP compiler accepts multiple disconnected sealed
  sub-volumes inside ``bsp_world``; each becomes a separate leaf cluster.

* **Outward-normals check** — The validator may flag a "false positive"
  because the sky envelope's inward-facing normals contribute a negative
  signed-volume term.  This does not prevent a successful Halo CE BSP
  compile; the flag is expected and can be ignored.

Usage
-----
Run inside Blender after ``generate_naranjos_dsl()`` has been executed::

    import importlib
    import halo_maps.naranjos.level as lvl
    importlib.reload(lvl)
    lvl.assemble_and_export()

Or from the command line::

    blender --background src/halo_maps/naranjos/blend/naranjos_dsl_updated.blend \\
        --python src/halo_maps/naranjos/level.py
"""

