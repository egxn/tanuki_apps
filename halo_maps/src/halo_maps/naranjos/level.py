def _ensure_collection(name: str) -> bpy.types.Collection:
    if name not in bpy.data.collections:
        coll = bpy.data.collections.new(name)
        bpy.context.scene.collection.children.link(coll)
    return bpy.data.collections[name]


def _move_to_collection(obj: bpy.types.Object, coll_name: str) -> None:
    coll = _ensure_collection(coll_name)
    for c in list(obj.users_collection):
        c.objects.unlink(obj)
    coll.objects.link(obj)


def _ensure_material(name: str, image_path: str | None = None) -> bpy.types.Material:
    """Get or create a Blender material.  If *image_path* is given and the
    material has no node tree yet, set up a Principled-BSDF + Image-Texture
    node graph so the texture is visible in the viewport and the image is
    packed into the .blend for later JMS path resolution."""
    mat = bpy.data.materials.get(name)
    if mat is None:
        mat = bpy.data.materials.new(name)

    if image_path:
        mat.use_nodes = True
        nodes = mat.node_tree.nodes
        links = mat.node_tree.links
        # Find existing Image Texture node, or build the full node tree from scratch
        tex = next((n for n in nodes if n.type == "TEX_IMAGE"), None)
        if tex is None:
            nodes.clear()
            out  = nodes.new("ShaderNodeOutputMaterial");  out.location  = (300, 0)
            bsdf = nodes.new("ShaderNodeBsdfPrincipled");  bsdf.location = (0, 0)
            tex  = nodes.new("ShaderNodeTexImage");         tex.location  = (-300, 0)
            links.new(tex.outputs["Color"], bsdf.inputs["Base Color"])
            links.new(bsdf.outputs["BSDF"], out.inputs["Surface"])
        # Always update the image so stale materials from previous runs are fixed
        try:
            img = bpy.data.images.load(image_path, check_existing=True)
            img.colorspace_settings.name = "sRGB"
            if bpy.data.filepath:
                try:
                    img.filepath = bpy.path.relpath(image_path)
                except ValueError:
                    pass
            tex.image = img
        except Exception as exc:
            print(f"[naranjos-level] WARNING: could not load {image_path}: {exc}")

    return mat


def _generate_box_uvs(
    bm: bmesh.types.BMesh,
    tile_by_mat: "dict[int, float] | None" = None,
    default_tile: float = 1.0,
) -> None:
    """Add a 'UVMap' UV layer using planar (box) projection.

    Each face is projected onto the plane perpendicular to its dominant normal
    axis and scaled by *tile_by_mat[face.material_index]* (or *default_tile*
    when the index is not in the dict).  Existing UV layers are replaced.
    """
    for layer in list(bm.loops.layers.uv.values()):
        bm.loops.layers.uv.remove(layer)
    uv = bm.loops.layers.uv.new("UVMap")
    tile_map = tile_by_mat or {}
    for face in bm.faces:
        tile_bu = tile_map.get(face.material_index, default_tile)
        inv = 1.0 / max(tile_bu, 1e-6)
        n = face.normal.normalized()
        ax, ay, az = abs(n.x), abs(n.y), abs(n.z)
        for loop in face.loops:
            co = loop.vert.co
            if ax >= ay and ax >= az:
                loop[uv].uv = (co.y * inv, co.z * inv)
            elif ay >= ax and ay >= az:
                loop[uv].uv = (co.x * inv, co.z * inv)
            else:
                loop[uv].uv = (co.x * inv, co.y * inv)


def _classify_face_material(
    face: "bmesh.types.BMFace", z_min: float, z_max: float
) -> int:
    """Return the material index for a building face.

    Vertical faces (|n.z| < 0.5) → WALL.
    Downward-facing horizontal → HALL (underside of slabs / ceilings).
    Upward-facing horizontal by fraction of building Z range:
        bottom 10 % → FLOOR, top 35 % → ROOF, rest → HALL.
    Adjust the 0.10 / 0.65 thresholds in-place if the classification looks
    wrong in Blender Material Preview.
    """
    nz = face.normal.normalized().z
    if abs(nz) < 0.5:
        return WALL_IDX
    if nz < 0.0:
        return HALL_IDX
    z_center = sum(v.co.z for v in face.verts) / len(face.verts)
    z_frac = (z_center - z_min) / max(z_max - z_min, 0.01)
    if z_frac < 0.10:
        return FLOOR_IDX
    if z_frac > 0.65:
        return ROOF_IDX
    return HALL_IDX


def _dissolve_coplanar_nm_edges(bm: "bmesh.types.BMesh", precision: int = 4) -> int:
    """Dissolve NM edges where ALL surrounding faces are on the same plane.

    These arise when a bisect splits a face flush with an adjacent coplanar face,
    leaving 3 coplanar faces sharing one edge.  Dissolving the edge merges them
    back into a single face (then re-triangulated later).

    Returns the number of edges dissolved.
    """
    def _pk(face):
        n = face.normal.normalized()
        if (n.x, n.y, n.z) < (0.0, 0.0, 0.0):
            n.negate()
        d   = -n.dot(face.verts[0].co)
        p   = 10 ** precision
        return (round(n.x * p), round(n.y * p), round(n.z * p), round(d * p))

    bm.edges.ensure_lookup_table()
    to_dissolve = [
        e for e in bm.edges
        if len(e.link_faces) > 2 and len({_pk(f) for f in e.link_faces}) == 1
    ]
    if not to_dissolve:
        return 0
    bmesh.ops.dissolve_edges(bm, edges=to_dissolve,
                             use_verts=False, use_face_split=False)
    bm.edges.ensure_lookup_table()
    bm.faces.ensure_lookup_table()
    return len(to_dissolve)


def _mark_nm_wall_faces_readonly(bm: "bmesh.types.BMesh") -> int:
    """Mark wall faces touching remaining NM edges as render-only (WALL_RO_IDX).

    After back-to-back and coplanar fixes, the remaining NM edges come from
    unavoidable 3-way staircase/wall junctions.  Assigning 'naranjos_wall!'
    (render-only, symbol '!') to those faces tells tool.exe to exclude them
    from collision NM checking while keeping them visible.

    This is a workaround for testing.  Production fix requires geometry redesign.

    Returns the number of faces re-marked.
    """
    bm.edges.ensure_lookup_table()
    bm.faces.ensure_lookup_table()

    nm_wall_faces: set = set()
    for e in bm.edges:
        if len(e.link_faces) > 2:
            for f in e.link_faces:
                if f.material_index == WALL_IDX:
                    nm_wall_faces.add(f)

    for f in nm_wall_faces:
        f.material_index = WALL_RO_IDX
    return len(nm_wall_faces)


def _load_source_into_bm(
    source: bpy.types.Object,
) -> bmesh.types.BMesh:
    """Evaluate *source* and load it into a fresh BMesh with world matrix applied.

    The caller owns the returned BMesh and must call ``bm.free()`` when done.
    """
    bpy.context.view_layer.update()
    depsgraph = bpy.context.evaluated_depsgraph_get()
    eval_obj  = source.evaluated_get(depsgraph)
    eval_mesh = eval_obj.to_mesh()

    bm = bmesh.new()
    bm.from_mesh(eval_mesh)
    eval_obj.to_mesh_clear()

    # Apply world matrix so the mesh is in world space.
    mat = source.matrix_world
    for v in bm.verts:
        v.co = mat @ v.co

    return bm


def _delete_duplicate_faces(bm: bmesh.types.BMesh, precision: int = 5) -> int:
    """Remove exact duplicate faces (same rounded vertex coordinate set).

    Runs a round-coordinate keying pass identical to the one already done
    inside ``_create_merged_bsp`` in ``generate_dsl.py``; kept here as a
    safety net for any duplicates introduced during subsequent merges.

    Returns the number of faces removed.
    """
    bm.faces.ensure_lookup_table()
    seen: dict = {}
    dupes: list = []
    for face in bm.faces:
        key = tuple(sorted(
            (round(v.co.x, precision), round(v.co.y, precision), round(v.co.z, precision))
            for v in face.verts
        ))
        if key in seen:
            dupes.append(face)
        else:
            seen[key] = face
    if dupes:
        bmesh.ops.delete(bm, geom=dupes, context="FACES")
        bm.faces.ensure_lookup_table()
    return len(dupes)


def _fix_coplanar_overlaps(
    bm: bmesh.types.BMesh,
    precision: int = 5,
    wall_mat_index: int | None = None,
    _skip_normal_update: bool = False,
) -> int:
    """Remove faces that overlap a coplanar neighbour using Sutherland-Hodgman clipping.

    For each coplanar pair (A, B) with no shared vertices, compute the 2-D
    intersection area.  If the overlap is significant (> 1 % of the smaller
    face's area), delete the smaller face.

    This replaces the old ``_all_verts_inside`` containment check, which
    required ALL vertices of B to be strictly inside A and therefore missed
    the common case where a small wall-cap triangle partially overlaps a
    larger union-fill triangle.

    Returns the number of faces removed.
    """

    # ── 2-D helpers ──────────────────────────────────────────────────────────

    def _poly_area(pts):
        """Signed area of a 2-D polygon (shoelace)."""
        a = 0.0
        n = len(pts)
        for i in range(n):
            x0, y0 = pts[i]
            x1, y1 = pts[(i + 1) % n]
            a += x0 * y1 - x1 * y0
        return a * 0.5

    def _line_isect(p1, p2, p3, p4):
        """Intersection of line p1→p2 with line p3→p4."""
        x1, y1 = p1;  x2, y2 = p2;  x3, y3 = p3;  x4, y4 = p4
        den = (x1 - x2) * (y3 - y4) - (y1 - y2) * (x3 - x4)
        if abs(den) < 1e-12:
            return p2
        px = ((x1 * y2 - y1 * x2) * (x3 - x4) - (x1 - x2) * (x3 * y4 - y3 * x4)) / den
        py = ((x1 * y2 - y1 * x2) * (y3 - y4) - (y1 - y2) * (x3 * y4 - y3 * x4)) / den
        return px, py

    def _inside_edge(p, a, b, ccw):
        """Return True if point p is inside the half-plane defined by edge a→b."""
        cross = (b[0] - a[0]) * (p[1] - a[1]) - (b[1] - a[1]) * (p[0] - a[0])
        return cross >= -1e-9 if ccw else cross <= 1e-9

    def _convex_clip(subject, clip):
        """Sutherland-Hodgman clipping; handles both CCW and CW clip polygons."""
        output = list(subject)
        ccw = _poly_area(clip) >= 0      # ← match winding of clip polygon
        n = len(clip)
        for i in range(n):
            if not output:
                break
            a = clip[(i - 1) % n]
            b = clip[i]
            inp = output
            output = []
            prev = inp[-1]
            for cur in inp:
                cur_in  = _inside_edge(cur,  a, b, ccw)
                prev_in = _inside_edge(prev, a, b, ccw)
                if cur_in:
                    if not prev_in:
                        output.append(_line_isect(prev, cur, a, b))
                    output.append(cur)
                elif prev_in:
                    output.append(_line_isect(prev, cur, a, b))
                prev = cur
        return output

    def _project(face):
        """Project face verts to 2-D by dropping the dominant normal axis."""
        n = face.normal.normalized()
        axis = max(range(3), key=lambda i: abs(n[i]))
        pts = []
        for v in face.verts:
            c = v.co
            if axis == 0:    pts.append((c.y, c.z))
            elif axis == 1:  pts.append((c.x, c.z))
            else:            pts.append((c.x, c.y))
        return pts

    def _plane_key(face):
        n = face.normal.normalized()
        if (n.x, n.y, n.z) < (0.0, 0.0, 0.0):
            n.negate()
        d = -n.dot(face.verts[0].co)
        p = 10 ** precision
        return (round(n.x * p), round(n.y * p), round(n.z * p), round(d * p))

    # ── Group faces by plane ──────────────────────────────────────────────────

    bm.faces.ensure_lookup_table()
    if not _skip_normal_update:
        bm.normal_update()
    groups: dict = {}
    for face in bm.faces:
        if len(face.verts) != 3:
            continue
        if wall_mat_index is not None and face.material_index != wall_mat_index:
            continue
        groups.setdefault(_plane_key(face), []).append(face)

    # ── Detect overlapping pairs; mark smaller for deletion ──────────────────

    to_delete: set = set()
    for faces in groups.values():
        if len(faces) < 2:
            continue
        # Pre-compute projections and areas
        proj = {f: _project(f) for f in faces}
        area = {f: abs(_poly_area(proj[f])) for f in faces}

        for i, a in enumerate(faces):
            if a in to_delete:
                continue
            pts_a = proj[a]
            area_a = area[a]
            if area_a < 1e-10:
                to_delete.add(a)
                continue
            for b in faces[i + 1:]:
                if b in to_delete:
                    continue
                # Skip pairs that share 2+ vertices (adjacent tiles / back-to-back walls)
                if sum(1 for v in a.verts if v in set(b.verts)) >= 2:
                    continue
                pts_b = proj[b]
                area_b = area[b]
                if area_b < 1e-10:
                    to_delete.add(b)
                    continue
                # Compute overlap via Sutherland-Hodgman
                clipped = _convex_clip(pts_a, pts_b)
                if len(clipped) < 3:
                    continue
                overlap = abs(_poly_area(clipped))
                if overlap < 1e-8:
                    continue
                # Delete the smaller face (the one more likely to be redundant)
                if area_a <= area_b:
                    to_delete.add(a)
                    break  # a is gone; move to next a
                else:
                    to_delete.add(b)

    if not to_delete:
        return 0

    # ── Safety check: prune to_delete to avoid unfillable open-path holes ────
    # Horizontal faces (|n.z| > 0.9) are ceiling/floor fills produced by
    # holes_fill in _union_horizontal_faces.  They are redundant fills, not
    # structural walls, so we bypass the safety check for them — any open
    # boundary chains they leave are patched by _fill_open_boundary_paths.
    # Vertical faces (structural walls) still go through the full safety check.
    HORIZ_Z = 0.9  # |n.z| threshold for "horizontal"

    horiz_delete: set = set()  # bypass safety check
    vert_delete: set = set()
    for _f in to_delete:
        if abs(_f.normal.normalized().z) > HORIZ_Z:
            horiz_delete.add(_f)
        else:
            vert_delete.add(_f)

    # Apply safety check only to the vertical subset.
    from collections import defaultdict as _dd

    safe_delete: set = set(vert_delete)

    for _guard in range(len(vert_delete) + 1):
        # Compute combined boundary edge-vertex degree map for vertical faces only.
        # Faces in horiz_delete are treated as "will be deleted" so they do NOT
        # count as surviving neighbours when computing remaining.
        all_deleting = safe_delete | horiz_delete
        deg: dict = _dd(int)
        edge_owner: dict = {}  # edge → face in safe_delete that contributes it
        for face in safe_delete:
            for e in face.edges:
                remaining = sum(1 for lf in e.link_faces if lf not in all_deleting)
                if remaining == 1:
                    deg[e.verts[0]] += 1
                    deg[e.verts[1]] += 1
                    edge_owner[e] = face

        open_verts = [v for v, d in deg.items() if d == 1]
        if not open_verts:
            break  # all would-be-boundary edges form closed loops → safe

        # Remove the face owning the first problematic (degree-1) edge.
        culprit = None
        for v in open_verts:
            for e, f in edge_owner.items():
                if v in e.verts and f in safe_delete:
                    culprit = f
                    break
            if culprit:
                break
        if culprit:
            safe_delete.discard(culprit)
        else:
            break  # can't improve further

    all_delete = safe_delete | horiz_delete
    if all_delete:
        bmesh.ops.delete(bm, geom=list(all_delete), context="FACES")
        bm.faces.ensure_lookup_table()
    return len(all_delete)


def _fix_back_to_back_nm_edges(bm: bmesh.types.BMesh) -> int:
    """Remove duplicate wall faces that create back-to-back NM edges.

    When two buildings share a wall plane, ``remove_doubles`` merges their
    boundary vertices so their wall triangles share edges → non-manifold
    (3+ faces per edge).  Specifically: building A generates wall faces with
    normal pointing *away* from A (canonical direction) and building B
    generates wall faces at the same plane pointing *away* from B (the opposite
    direction = anti-canonical).  After the vertex merge they share edges.

    Strategy
    --------
    1. Find every NM edge whose link_faces have *both* a canonical group
       (normal (x,y,z) ≥ (0,0,0)) and an anti-canonical group.
    2. Collect every anti-canonical face that participates in at least one such
       NM edge.  These are the "duplicate" wall faces from building B.
    3. Delete them in one go, leaving closed boundary loops that the caller
       fills with portal faces.

    Returns the number of faces deleted.
    """
    bm.verts.ensure_lookup_table()
    bm.edges.ensure_lookup_table()
    bm.faces.ensure_lookup_table()

    def _is_canonical(face: bmesh.types.BMFace) -> bool:
        n = face.normal.normalized()
        return (n.x, n.y, n.z) >= (0.0, 0.0, 0.0)

    to_delete: set = set()

    for e in bm.edges:
        if len(e.link_faces) < 3:
            continue
        link = list(e.link_faces)
        has_canon = any(_is_canonical(f) for f in link)
        has_anti  = any(not _is_canonical(f) for f in link)
        if has_canon and has_anti:
            for f in link:
                if not _is_canonical(f):
                    to_delete.add(f)

    if not to_delete:
        return 0

    bmesh.ops.delete(bm, geom=list(to_delete), context="FACES")
    bm.verts.ensure_lookup_table()
    bm.edges.ensure_lookup_table()
    bm.faces.ensure_lookup_table()
    bm.normal_update()
    return len(to_delete)


def _fill_boundary_with_portal(bm: bmesh.types.BMesh) -> int:
    """Fill every open boundary-edge loop with a face, assign PORTAL_IDX.

    Uses ``bmesh.ops.holes_fill`` which finds closed loops of boundary edges
    and inserts one spanning face per loop.  Each new face gets material slot
    ``PORTAL_IDX`` so Halo CE treats the opening as a portal (invisible,
    passable) rather than a solid wall.

    Returns the number of portal faces added.
    """
    bm.edges.ensure_lookup_table()
    boundary = [e for e in bm.edges if len(e.link_faces) < 2]
    if not boundary:
        return 0

    result = bmesh.ops.holes_fill(bm, edges=boundary, sides=0)
    new_faces = result.get("faces", [])
    for f in new_faces:
        f.material_index = PORTAL_IDX
    return len(new_faces)


def _fill_open_boundary_paths(bm: bmesh.types.BMesh) -> int:
    """Patch open boundary-edge chains that ``holes_fill`` cannot close.

    When a coplanar face is deleted its edges may leave 3 boundary edges that
    form a *path* (two endpoint vertices, no closed loop).  ``holes_fill``
    requires a closed loop; it silently skips open paths.

    For each such open path we add wall triangles to close the gap:
    * Walk the boundary chain from one endpoint to the other collecting
      the ordered vertex list V = [v0, v1, …, vN].
    * Triangulate the fan V[0]-V[i]-V[i+1] for i in 1..N-1 and assign
      WALL_IDX to each new triangle.

    Returns the number of faces added.
    """
    bm.verts.ensure_lookup_table()
    bm.edges.ensure_lookup_table()

    boundary_edges = [e for e in bm.edges if len(e.link_faces) < 2]
    if not boundary_edges:
        return 0

    # Build adjacency: vert → list of boundary-edge neighbours
    from collections import defaultdict
    adj: dict = defaultdict(list)
    for e in boundary_edges:
        a, b = e.verts
        adj[a].append(b)
        adj[b].append(a)

    # Find connected components of boundary-edge graph
    visited_verts: set = set()
    components: list = []
    for start in adj:
        if start in visited_verts:
            continue
        comp_verts: set = set()
        stack = [start]
        while stack:
            v = stack.pop()
            if v in comp_verts:
                continue
            comp_verts.add(v)
            stack.extend(nb for nb in adj[v] if nb not in comp_verts)
        visited_verts |= comp_verts
        components.append(comp_verts)

    n_added = 0
    for comp in components:
        # Degree-1 vertices are endpoints of an open chain.
        endpoints = [v for v in comp if len(adj[v]) == 1]
        if not endpoints:
            continue  # closed loop — holes_fill handles it

        # Walk the chain from one endpoint to the other.
        start = endpoints[0]
        chain: list = [start]
        prev = None
        cur = start
        while True:
            nbs = [nb for nb in adj[cur] if nb is not prev]
            if not nbs:
                break
            nxt = nbs[0]
            chain.append(nxt)
            prev, cur = cur, nxt
            if len(adj[cur]) == 1 and cur is not start:
                break  # reached other endpoint

        if len(chain) < 3:
            continue

        # Fan-triangulate the chain polygon.
        anchor = chain[0]
        for i in range(1, len(chain) - 1):
            v0 = anchor
            v1 = chain[i]
            v2 = chain[i + 1]
            try:
                face = bm.faces.new([v0, v1, v2])
                face.material_index = WALL_IDX
                n_added += 1
            except ValueError:
                pass  # face already exists

    if n_added:
        bm.faces.ensure_lookup_table()
        bm.normal_update()
    return n_added


def _add_sky_envelope(
    bm: bmesh.types.BMesh,
    x0: float, x1: float,
    y0: float, y1: float,
    z_bot: float, z_top: float,
) -> None:
    """Append a 6-face sky envelope with inward-facing normals to *bm*.

    The envelope is a closed box large enough to surround the entire campus.
    All six faces are assigned ``SKY_IDX`` so that Halo CE renders them as
    open sky.  Normals are flipped inward (toward the campus interior) as
    required by Halo CE's BSP convention.

    The new geometry is appended to *bm* without merging vertices with
    existing geometry — the sky envelope forms its own separate sealed volume.
    """
    v000 = bm.verts.new((x0, y0, z_bot))
    v100 = bm.verts.new((x1, y0, z_bot))
    v110 = bm.verts.new((x1, y1, z_bot))
    v010 = bm.verts.new((x0, y1, z_bot))
    v001 = bm.verts.new((x0, y0, z_top))
    v101 = bm.verts.new((x1, y0, z_top))
    v111 = bm.verts.new((x1, y1, z_top))
    v011 = bm.verts.new((x0, y1, z_top))

    # 6 faces with CCW winding (outward normals initially)
    sky_faces = [
        bm.faces.new([v001, v011, v111, v101]),  # top
        bm.faces.new([v000, v100, v110, v010]),  # bottom
        bm.faces.new([v000, v001, v101, v100]),  # front  (y = y0)
        bm.faces.new([v110, v111, v011, v010]),  # back   (y = y1)
        bm.faces.new([v000, v010, v011, v001]),  # left   (x = x0)
        bm.faces.new([v100, v101, v111, v110]),  # right  (x = x1)
    ]

    # Flip inward — Halo CE requires normals to face the playable area.
    bmesh.ops.reverse_faces(bm, faces=sky_faces)

    for f in sky_faces:
        f.material_index = SKY_IDX


