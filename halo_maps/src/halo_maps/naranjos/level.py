def assemble_naranjos_level(
    bsp_source_name: str = BSP_SOURCE_NAME,
    output_name: str = BSP_WORLD_NAME,
    sky_height_bu: float = SKY_HEIGHT_BU,
    ground_margin_bu: float = GROUND_MARGIN_BU,
) -> bpy.types.Object | None:
    """Build the final ``bsp_world`` object from buildings + sky envelope.

    All mesh operations use bmesh directly, avoiding ``bpy.ops.object.join``
    and its implicit vertex-merge semantics.

    Sealing strategy
    ~~~~~~~~~~~~~~~~
    * Building door / window openings are filled with ``+portal`` faces so
      that ``bsp_merged`` itself becomes a sealed manifold.
    * A sky envelope (closed box, inward normals) is appended as a second
      disconnected sealed volume.
    * No separate ground plane is created: ``bsp_merged`` already includes
      the campus paving slab generated by ``generate_naranjos_dsl()``.

    Parameters
    ----------
    bsp_source_name:
        Name of the merged building object created by ``generate_naranjos_dsl()``.
    output_name:
        Name for the assembled, ready-to-export BSP object.
    sky_height_bu:
        Minimum top of the sky envelope above z = 0 (Blender units).
    ground_margin_bu:
        Space added around the campus bounding box for the sky envelope walls.

    Returns
    -------
    The assembled ``bsp_world`` object, or ``None`` if the source was missing.
    """
    source = bpy.data.objects.get(bsp_source_name)
    if source is None:
        print(
            f"[naranjos-level] ERROR: '{bsp_source_name}' not found. "
            "Run generate_naranjos_dsl() first."
        )
        return None

    # ── 1. Load bsp_merged into a working bmesh ────────────────────────────
    bm = _load_source_into_bm(source)
    bm.verts.ensure_lookup_table()
    bm.edges.ensure_lookup_table()
    bm.faces.ensure_lookup_table()
    print(
        f"[naranjos-level] bsp_merged loaded: "
        f"{len(bm.verts)} verts, {len(bm.faces)} faces"
    )

    # ── 2. Assign WALL_IDX to every face ──────────────────────────────────
    for f in bm.faces:
        f.material_index = WALL_IDX

    # ── 2b. Fix anti-parallel back-to-back NM edges ───────────────────────
    n_btb = _fix_back_to_back_nm_edges(bm)
    if n_btb:
        print(f"[naranjos-level] Removed {n_btb} back-to-back NM face(s)")

    # ── 3. Safety: remove any exact duplicate faces ────────────────────────
    n_dup = _delete_duplicate_faces(bm)
    if n_dup:
        print(f"[naranjos-level] Removed {n_dup} duplicate faces")

    # ── 4. Fill open boundary edges with +portal faces ────────────────────
    bm.edges.ensure_lookup_table()
    n_boundary_before = sum(1 for e in bm.edges if len(e.link_faces) < 2)
    print(f"[naranjos-level] Boundary edges before portal fill: {n_boundary_before}")

    n_portal = _fill_boundary_with_portal(bm)
    print(f"[naranjos-level] Portal faces added: {n_portal}")

    bm.edges.ensure_lookup_table()
    n_boundary_after = sum(1 for e in bm.edges if len(e.link_faces) < 2)
    if n_boundary_after:
        print(
            f"[naranjos-level] WARNING: {n_boundary_after} boundary edges remain "
            "after portal fill — complex topology or non-planar loops detected."
        )
    else:
        print("[naranjos-level] Buildings sealed ✓")

    # ── 5. Compute campus bounding box from the (now clean) building mesh ──
    xs = [v.co.x for v in bm.verts]
    ys = [v.co.y for v in bm.verts]
    zs = [v.co.z for v in bm.verts]
    x_min, x_max = min(xs), max(xs)
    y_min, y_max = min(ys), max(ys)
    z_min, z_max = min(zs), max(zs)
    print(
        f"[naranjos-level] Campus bounds: "
        f"X=[{x_min:.2f}, {x_max:.2f}]  "
        f"Y=[{y_min:.2f}, {y_max:.2f}]  "
        f"Z=[{z_min:.2f}, {z_max:.2f}] BU"
    )

    # ── 6. Sky envelope parameters ─────────────────────────────────────────
    gx0 = x_min - ground_margin_bu
    gx1 = x_max + ground_margin_bu
    gy0 = y_min - ground_margin_bu
    gy1 = y_max + ground_margin_bu
    sky_top = max(z_max + 5.0, float(sky_height_bu))
    sky_bot = z_min - 1.0      # 1 BU below the lowest building geometry

    print(
        f"[naranjos-level] Sky envelope: "
        f"X=[{gx0:.1f}, {gx1:.1f}]  "
        f"Y=[{gy0:.1f}, {gy1:.1f}]  "
        f"Z=[{sky_bot:.1f}, {sky_top:.1f}]  "
        f"(height={sky_top - sky_bot:.1f} BU)"
    )

    # ── 7. Append sky envelope geometry ───────────────────────────────────
    _add_sky_envelope(bm, gx0, gx1, gy0, gy1, sky_bot, sky_top)

    # ── 8. Triangulate everything ─────────────────────────────────────────
    bm.faces.ensure_lookup_table()
    bmesh.ops.triangulate(bm, faces=bm.faces)
    bm.normal_update()   # ← required before any normal-based grouping

    # ── 8a. Dissolve coplanar NM edges ────────────────────────────────────
    n_cp_nm = _dissolve_coplanar_nm_edges(bm)
    if n_cp_nm:
        print(f"[naranjos-level] Dissolved {n_cp_nm} coplanar NM edge(s)")
        ngons = [f for f in bm.faces if len(f.verts) > 3]
        if ngons:
            bmesh.ops.triangulate(bm, faces=ngons)
        bm.normal_update()

    # ── 8b. Remove coplanar overlapping faces introduced by triangulation ──
    # Only consider wall faces (slot 0); portals and sky are intentional quads.
    n_overlap = _fix_coplanar_overlaps(bm, wall_mat_index=WALL_IDX)
    if n_overlap:
        print(f"[naranjos-level] Removed {n_overlap} coplanar-overlap faces")
        # Re-seal any boundary edges that coplanar removal may have opened.
        bm.edges.ensure_lookup_table()
        n_new_boundary = sum(1 for e in bm.edges if len(e.link_faces) < 2)
        if n_new_boundary:
            n_resealed = _fill_boundary_with_portal(bm)
            print(f"[naranjos-level] Re-sealed {n_resealed} portal(s) after coplanar fix")
            # Triangulate the newly added portal faces (holes_fill creates ngons)
            bm.faces.ensure_lookup_table()
            new_portal_ngons = [
                f for f in bm.faces
                if f.material_index == PORTAL_IDX and len(f.verts) > 3
            ]
            if new_portal_ngons:
                bmesh.ops.triangulate(bm, faces=new_portal_ngons)
                print(
                    f"[naranjos-level] Triangulated {len(new_portal_ngons)} new portal ngon(s)"
                )
    else:
        print("[naranjos-level] No coplanar overlaps found ✓")

    # ── 8c. Fix any T-junctions introduced by portal-fill triangulation ───────
    # The ngon portals created by _fill_boundary_with_portal are triangulated
    # in steps 8 and 8b.  Those new diagonal edges may pass through existing
    # vertices, creating T-junctions not present in bsp_merged.  Run the same
    # targeted T-junction splitter used in generate_dsl to clean them up, then
    # re-triangulate any ngons it creates.
    from halo_maps.naranjos.generate_dsl import (
        _fix_t_junctions_targeted,
        _fix_t_junctions_nonmanifold,
        MERGE_DISTANCE_BU as _MERGE_DIST,
    )
    total_tj_fixed = 0
    for _pass in range(5):
        bm.verts.ensure_lookup_table()
        bm.edges.ensure_lookup_table()
        bm.faces.ensure_lookup_table()
        n_tj1 = _fix_t_junctions_targeted(bm)
        if n_tj1:
            bmesh.ops.remove_doubles(bm, verts=bm.verts, dist=_MERGE_DIST)
        n_tj2 = _fix_t_junctions_nonmanifold(bm)
        if n_tj2:
            bmesh.ops.remove_doubles(bm, verts=bm.verts, dist=_MERGE_DIST)
        total_tj_fixed += n_tj1 + n_tj2
        if not n_tj1 and not n_tj2:
            break
    # Re-triangulate any ngons created by edge splits above.
    bm.faces.ensure_lookup_table()
    ngons = [f for f in bm.faces if len(f.verts) > 3]
    if ngons:
        bmesh.ops.triangulate(bm, faces=ngons)
        print(f"[naranjos-level] Re-triangulated {len(ngons)} ngon(s) after T-junction fix")
    if total_tj_fixed:
        print(f"[naranjos-level] T-junction fix: {total_tj_fixed} split(s) total")
    else:
        print("[naranjos-level] No T-junctions in bsp_world ✓")

    # ── 8d. Final seal + coplanar cleanup after T-junction re-triangulation ───
    # Steps 8b/8c can introduce new boundary edges (from bisect_plane cuts that
    # don't form closed loops) and new coplanar overlaps (from new diagonals
    # landing inside existing coplanar triangles).  Run one last fix pass.
    bm.edges.ensure_lookup_table()
    n_final_boundary = sum(1 for e in bm.edges if len(e.link_faces) < 2)
    if n_final_boundary:
        n_final_portal = _fill_boundary_with_portal(bm)
        print(f"[naranjos-level] Final re-seal: {n_final_portal} portal(s) added")
        # Triangulate any new portal ngons
        bm.faces.ensure_lookup_table()
        new_ngons = [
            f for f in bm.faces
            if f.material_index == PORTAL_IDX and len(f.verts) > 3
        ]
        if new_ngons:
            bmesh.ops.triangulate(bm, faces=new_ngons)

    bm.normal_update()
    n_final_overlap = _fix_coplanar_overlaps(bm, wall_mat_index=WALL_IDX)
    if n_final_overlap:
        print(f"[naranjos-level] Final coplanar cleanup: removed {n_final_overlap} face(s)")
        # Re-seal anything opened by this final cleanup
        bm.edges.ensure_lookup_table()
        n_post = sum(1 for e in bm.edges if len(e.link_faces) < 2)
        if n_post:
            n_post_portal = _fill_boundary_with_portal(bm)
            print(f"[naranjos-level] Post-cleanup re-seal: {n_post_portal} portal(s)")
            bm.faces.ensure_lookup_table()
            post_ngons = [
                f for f in bm.faces
                if f.material_index == PORTAL_IDX and len(f.verts) > 3
            ]
            if post_ngons:
                bmesh.ops.triangulate(bm, faces=post_ngons)
    else:
        print("[naranjos-level] No residual coplanar overlaps ✓")

    # ── 8e. Report remaining NM edges (back-to-back walls) ────────────────
    # NM edges from adjacent buildings sharing wall planes are resolved at the
    # generate_dsl level (see generate_dsl._remove_back_to_back_walls).
    # Late coplanar NM dissolve: portal sealing / triangulation can create a few
    # coplanar overlapping faces (portal-fill coincident with a wall face).
    bm.edges.ensure_lookup_table()
    n_nm = sum(1 for e in bm.edges if len(e.link_faces) > 2)
    if n_nm:
        n_cp2 = _dissolve_coplanar_nm_edges(bm)
        if n_cp2:
            bm.faces.ensure_lookup_table()
            ngons = [f for f in bm.faces if len(f.verts) > 3]
            if ngons:
                bmesh.ops.triangulate(bm, faces=ngons)
            bm.normal_update()
            bm.edges.ensure_lookup_table()
            n_nm = sum(1 for e in bm.edges if len(e.link_faces) > 2)
            print(f"[naranjos-level] Dissolved {n_cp2} late coplanar NM edge(s)")

    # Classify remaining NM edges: only those whose 3+ faces are ALL collideable
    # (not +portal / +sky / render-only) are real tool.exe "couldn't update edge"
    # errors.  An edge with 2 collideable + 1 portal face is manifold for
    # collision (tool.exe processes portals separately), so it is harmless.
    if n_nm:
        coll_nm = 0
        for e in bm.edges:
            if len(e.link_faces) <= 2:
                continue
            coll_faces = sum(
                1 for f in e.link_faces
                if f.material_index not in (PORTAL_IDX, SKY_IDX, WALL_RO_IDX)
            )
            if coll_faces > 2:
                coll_nm += 1
        if coll_nm:
            print(f"[naranjos-level] WARNING: {coll_nm} collideable NM edge(s) remain "
                  f"(of {n_nm} total) — real tool.exe errors")
        else:
            print(f"[naranjos-level] {n_nm} NM edge(s) remain, all on portal/sky/"
                  f"render-only faces → harmless for tool.exe collision ✓")
    else:
        print("[naranjos-level] No NM edges ✓")

    n_verts = len(bm.verts)
    n_faces = len(bm.faces)
    print(
        f"[naranjos-level] '{output_name}' assembled: "
        f"{n_verts} verts, {n_faces} triangles"
    )

    # ── 8f. Hide source mesh so it doesn't overlap bsp_world in the viewport ──
    src_obj = bpy.data.objects.get(bsp_source_name)
    if src_obj is not None:
        src_obj.hide_viewport = True
        src_obj.hide_render   = True

    # ── 9. Write bmesh into a new Mesh datablock ───────────────────────────
    # Remove any stale bsp_world from previous runs.
    old = bpy.data.objects.get(output_name)
    if old is not None:
        bpy.data.objects.remove(old, do_unlink=True)
    # Remove stale mesh datablock with the same name, if any.
    old_mesh = bpy.data.meshes.get(output_name)
    if old_mesh is not None:
        bpy.data.meshes.remove(old_mesh)

    result_mesh = bpy.data.meshes.new(output_name)
    bm.to_mesh(result_mesh)
    bm.free()
    result_mesh.update()

    # ── 10. Assign material slots in index order ───────────────────────────
    #   0 WALL_IDX    → naranjos_wall    (vertical walls)
    #   1 FLOOR_IDX   → naranjos_floor   (ground-level paving)
    #   2 HALL_IDX    → naranjos_hall    (indoor floors / slab undersides)
    #   3 ROOF_IDX    → naranjos_roof    (building rooftops)
    #   4 WALL_RO_IDX → naranjos_wall!   (render-only, NM-edge workaround)
    #   5 PORTAL_IDX  → +portal
    #   6 SKY_IDX     → +sky
    import os as _os
    result_mesh.materials.append(_ensure_material(WALL_MAT,    image_path=WALL_TIF  if _os.path.isfile(WALL_TIF)  else None))
    result_mesh.materials.append(_ensure_material(FLOOR_MAT,   image_path=FLOOR_TIF if _os.path.isfile(FLOOR_TIF) else None))
    result_mesh.materials.append(_ensure_material(HALL_MAT,    image_path=HALL_TIF  if _os.path.isfile(HALL_TIF)  else None))
    result_mesh.materials.append(_ensure_material(ROOF_MAT,    image_path=ROOF_TIF  if _os.path.isfile(ROOF_TIF)  else None))
    result_mesh.materials.append(_ensure_material(WALL_RO_MAT, image_path=WALL_TIF  if _os.path.isfile(WALL_TIF)  else None))
    result_mesh.materials.append(_ensure_material(PORTAL_MAT))
    result_mesh.materials.append(_ensure_material(SKY_MAT))

    # ── 11. Create and link the bsp_world object ───────────────────────────
    # Created BEFORE the read-back so that step 9b can use the evaluator
    # (eval_obj.to_mesh()), the same code-path validate_bsp uses.
    bsp_obj = bpy.data.objects.new(output_name, result_mesh)
    bsp_obj.location = (0.0, 0.0, 0.0)
    _ensure_collection("BSP").objects.link(bsp_obj)
    bpy.context.view_layer.objects.active = bsp_obj

    # ── 9b. Post-write coplanar verification (read-back loop via evaluator) ─
    # Load the mesh via the depsgraph evaluator (exactly as validate_bsp does)
    # so that plane-key precision matches.  Repeat until stable (max 5 passes).
    for _rb_iter in range(5):
        bpy.context.view_layer.update()
        _depsgraph = bpy.context.evaluated_depsgraph_get()
        _eval_obj  = bsp_obj.evaluated_get(_depsgraph)
        _eval_mesh = _eval_obj.to_mesh()
        bm_check = bmesh.new()
        bm_check.from_mesh(_eval_mesh)
        _eval_obj.to_mesh_clear()
        bm_check.verts.ensure_lookup_table()
        bm_check.edges.ensure_lookup_table()
        bm_check.faces.ensure_lookup_table()
        # Do NOT call normal_update: normals come from the evaluated mesh's
        # stored float32 normals, identical to what validate_bsp will see.
        n_readback = _fix_coplanar_overlaps(bm_check, wall_mat_index=WALL_IDX,
                                            _skip_normal_update=True)
        bm_check.edges.ensure_lookup_table()
        n_rb_bnd = sum(1 for e in bm_check.edges if len(e.link_faces) < 2)
        rb_modified = n_readback > 0 or n_rb_bnd > 0

        if n_readback:
            print(f"[naranjos-level] Read-back[{_rb_iter}] coplanar fix: "
                  f"removed {n_readback} face(s)")
        if n_rb_bnd:
            print(f"[naranjos-level] Read-back[{_rb_iter}] boundary: "
                  f"{n_rb_bnd} edge(s)")

        if rb_modified:
            if n_rb_bnd:
                n_rb_portal = _fill_boundary_with_portal(bm_check)
                if n_rb_portal:
                    print(f"[naranjos-level] Read-back[{_rb_iter}] re-seal: "
                          f"{n_rb_portal} portal(s)")
                bm_check.faces.ensure_lookup_table()
                rb_ngons = [
                    f for f in bm_check.faces
                    if f.material_index == PORTAL_IDX and len(f.verts) > 3
                ]
                if rb_ngons:
                    bmesh.ops.triangulate(bm_check, faces=rb_ngons)
                bm_check.edges.ensure_lookup_table()
                remaining_bnd = sum(1 for e in bm_check.edges if len(e.link_faces) < 2)
                if remaining_bnd:
                    n_patched = _fill_open_boundary_paths(bm_check)
                    if n_patched:
                        print(f"[naranjos-level] Read-back[{_rb_iter}] "
                              f"patched {n_patched} open-path triangle(s)")
                        bm_check.normal_update()
                        _fix_coplanar_overlaps(bm_check, wall_mat_index=WALL_IDX)
                    bm_check.edges.ensure_lookup_table()
                    still_bnd = sum(1 for e in bm_check.edges if len(e.link_faces) < 2)
                    if still_bnd:
                        print(f"[naranjos-level] WARNING: {still_bnd} boundary "
                              f"edge(s) remain after read-back patching")
            bm_check.to_mesh(result_mesh)
            result_mesh.update()
            bm_check.free()
        else:
            print("[naranjos-level] Read-back: mesh verified clean ✓")
            bm_check.free()
            break  # stable — no more overlaps or boundary edges

    # ── 9c. Reclassify building faces into WALL / FLOOR / HALL / ROOF ────────
    # Done after the read-back loop so repair triangles are also classified.
    bpy.context.view_layer.update()
    _dg_f = bpy.context.evaluated_depsgraph_get()
    _ev_f  = bsp_obj.evaluated_get(_dg_f)
    _em_f  = _ev_f.to_mesh()
    bm_final = bmesh.new()
    bm_final.from_mesh(_em_f)
    _ev_f.to_mesh_clear()
    bm_final.faces.ensure_lookup_table()
    bm_final.normal_update()

    _bz = [v.co.z
           for f in bm_final.faces if f.material_index == WALL_IDX
           for v in f.verts]
    if _bz:
        _z_min, _z_max = min(_bz), max(_bz)
        _n_recl = 0
        for f in bm_final.faces:
            if f.material_index == WALL_IDX:
                new_mi = _classify_face_material(f, _z_min, _z_max)
                if new_mi != WALL_IDX:
                    f.material_index = new_mi
                    _n_recl += 1
        _counts = " | ".join(
            f"{lbl}={sum(1 for f in bm_final.faces if f.material_index == idx)}"
            for lbl, idx in [("wall", WALL_IDX), ("floor", FLOOR_IDX),
                              ("hall", HALL_IDX),  ("roof", ROOF_IDX)]
        )
        print(
            f"[naranjos-level] Material classification "
            f"(z=[{_z_min:.2f}, {_z_max:.2f}] BU): {_counts}"
        )

    # ── 9d. Per-material box-mapped UVs ────────────────────────────────────────
    _generate_box_uvs(bm_final, tile_by_mat={
        WALL_IDX:    WALL_TILE_BU,
        FLOOR_IDX:   FLOOR_TILE_BU,
        HALL_IDX:    HALL_TILE_BU,
        ROOF_IDX:    ROOF_TILE_BU,
        WALL_RO_IDX: WALL_TILE_BU,
    })

    bm_final.to_mesh(result_mesh)
    result_mesh.update()
    bm_final.free()
    print("[naranjos-level] Per-material UVs generated ✓")

    return bsp_obj


def validate_and_report(obj_name: str = BSP_WORLD_NAME) -> bool:
    """Run BSP validation on *obj_name* and print a summary.

    Returns ``True`` if all checks pass, ``False`` otherwise.

    Note: the *outward normals* check may report a false positive for
    ``bsp_world`` because the sky envelope's inward normals give a negative
    signed-volume contribution.  This is expected and does not prevent a
    successful Halo CE BSP compile.
    """
    bpy.context.view_layer.update()

    # ── Pre-validate coplanar cleanup (validate-exact) ────────────────────
    # Use validate_bsp's own _plane_key / _project_tri / _convex_clip so the
    # plane-key computation is guaranteed identical to what validate will use.
    from halo_maps.naranjos.validate_bsp import (
        _plane_key    as _vbsp_pk,
        _project_tri  as _vbsp_proj,
        _convex_clip  as _vbsp_clip,
        _tri_area_2d  as _vbsp_area,
        AREA_TOL      as _VBSP_AREA_TOL,
    )
    _HORIZ_Z = 0.9   # |n.z| > threshold → horizontal fill face

    def _pv_fix(bm_pv):
        """One-pass coplanar fix using validate_bsp's exact algorithm."""
        groups_pv: dict = {}
        for _f in bm_pv.faces:
            if len(_f.verts) == 3 and _f.material_index == WALL_IDX:
                groups_pv.setdefault(_vbsp_pk(_f), []).append(_f)

        to_del_pv: set = set()
        for _faces_pv in groups_pv.values():
            if len(_faces_pv) < 2:
                continue
            for _i, _fa in enumerate(_faces_pv):
                if _fa in to_del_pv:
                    continue
                _va_set = set(_fa.verts)
                _tri_a  = list(_vbsp_proj(_fa))
                _area_a = abs(_vbsp_area(_tri_a))
                if _area_a < _VBSP_AREA_TOL:
                    to_del_pv.add(_fa)
                    continue
                _na = _fa.normal.normalized()
                for _fb in _faces_pv[_i + 1:]:
                    if _fb in to_del_pv:
                        continue
                    # Skip anti-parallel pairs (back-to-back walls — one face
                    # per room side on the same partition plane).  These appear
                    # in the same plane group because _plane_key canonicalises
                    # the normal, but they are valid BSP geometry and their
                    # apparent 2-D overlap is intentional.
                    if _na.dot(_fb.normal.normalized()) < -0.5:
                        continue
                    if len(_va_set.intersection(_fb.verts)) >= 2:
                        continue
                    _inter = _vbsp_clip(_tri_a, list(_vbsp_proj(_fb)))
                    if len(_inter) < 3:
                        continue
                    if abs(_vbsp_area(_inter)) > _VBSP_AREA_TOL:
                        _area_b = abs(_vbsp_area(list(_vbsp_proj(_fb))))
                        if _area_a <= _area_b:
                            to_del_pv.add(_fa)
                            break
                        else:
                            to_del_pv.add(_fb)

        # Horizontal fills: bypass safety check; vertical: apply it.
        _horiz_del = {_f for _f in to_del_pv
                      if abs(_f.normal.normalized().z) > _HORIZ_Z}
        _vert_del  = to_del_pv - _horiz_del
        # Safety check on vertical faces only.
        from collections import defaultdict as _dd_pv
        _safe = set(_vert_del)
        for _ in range(len(_vert_del) + 1):
            _all_del = _safe | _horiz_del
            _deg: dict = _dd_pv(int)
            _eo: dict  = {}
            for _f in _safe:
                for _e in _f.edges:
                    _rem = sum(1 for _lf in _e.link_faces if _lf not in _all_del)
                    if _rem == 1:
                        _deg[_e.verts[0]] += 1
                        _deg[_e.verts[1]] += 1
                        _eo[_e] = _f
            _ov = [_v for _v, _d in _deg.items() if _d == 1]
            if not _ov:
                break
            _culp = None
            for _v in _ov:
                for _e, _f in _eo.items():
                    if _v in _e.verts and _f in _safe:
                        _culp = _f; break
                if _culp:
                    break
            if _culp:
                _safe.discard(_culp)
            else:
                break

        _all_delete = _safe | _horiz_del
        if _all_delete:
            bmesh.ops.delete(bm_pv, geom=list(_all_delete), context="FACES")
        return len(_all_delete)

    _obj = bpy.data.objects.get(obj_name)
    if _obj is not None and _obj.type == "MESH":
        for _pv_pass in range(5):
            _dg = bpy.context.evaluated_depsgraph_get()
            _ev = _obj.evaluated_get(_dg)
            _em = _ev.to_mesh()
            _bm = bmesh.new()
            _bm.from_mesh(_em)
            _ev.to_mesh_clear()
            _bm.verts.ensure_lookup_table()
            _bm.edges.ensure_lookup_table()
            _bm.faces.ensure_lookup_table()
            _n_fix = _pv_fix(_bm)
            _bm.edges.ensure_lookup_table()
            _n_bnd = sum(1 for e in _bm.edges if len(e.link_faces) < 2)
            if _n_fix or _n_bnd:
                if _n_bnd:
                    _fill_boundary_with_portal(_bm)
                    _bm.faces.ensure_lookup_table()
                    _new_ngons = [f for f in _bm.faces
                                  if f.material_index == PORTAL_IDX and len(f.verts) > 3]
                    if _new_ngons:
                        bmesh.ops.triangulate(_bm, faces=_new_ngons)
                print(f"[naranjos-level] Pre-validate fix[{_pv_pass}]: "
                      f"removed {_n_fix} overlap(s), boundary={_n_bnd}")
                _bm.to_mesh(_obj.data)
                _obj.data.update()
                _bm.free()
                bpy.context.view_layer.update()
            else:
                _bm.free()
                break  # clean — no changes needed

    print(f"\n[naranjos-level] ── Validating '{obj_name}' ──")
    ok = validate(obj_name)
    verdict = "✓ all checks passed" if ok else "✗ issues found — see above"
    print(f"[naranjos-level] {verdict}")
    return ok


def export_level(
    obj_name: str = BSP_WORLD_NAME,
    output_path: str | None = None,
    map_name: str = "naranjos",
    scale: float = 1.0,
) -> str:
    """Export *obj_name* to JMS v8200 format.

    Parameters
    ----------
    obj_name:
        Name of the BSP mesh object to export (default ``"bsp_world"``).
    output_path:
        Destination ``.jms`` file.  Defaults to ``<naranjos_dir>/naranjos.jms``.
    map_name:
        Embedded label in the JMS comment header.
    scale:
        Uniform scale applied to all vertex positions in the JMS output.
        Equivalent to S→<scale>→Enter in Blender Edit Mode.  Use ``27.0``
        to match the Halo CE world-unit convention (1 BU = 0.55 m).

    Returns
    -------
    Absolute path of the written ``.jms`` file.
    """
    if output_path is None:
        output_path = DEFAULT_JMS_PATH

    path = export_jms(obj_name, output_path, map_name=map_name, scale=scale)
    scale_note = f" (scale ×{scale})" if scale != 1.0 else ""
    print(f"[naranjos-level] JMS written to: {path}{scale_note}")
    return path


def assemble_and_export(
    bsp_source_name: str = BSP_SOURCE_NAME,
    output_name: str = BSP_WORLD_NAME,
    sky_height_bu: float = SKY_HEIGHT_BU,
    ground_margin_bu: float = GROUND_MARGIN_BU,
    export_path: str | None = None,
    jms_scale: float = 1.0,
) -> None:
    """Full pipeline: assemble the Naranjos level and export to JMS.

    This is the single entry-point for the complete level-creation workflow.
    Run it inside Blender after ``generate_naranjos_dsl()`` has been executed::

        import importlib
        import halo_maps.naranjos.level as lvl
        importlib.reload(lvl)
        lvl.assemble_and_export(jms_scale=27.0)

    Parameters
    ----------
    bsp_source_name:
        Source object from ``generate_naranjos_dsl()`` (default ``"bsp_merged"``).
    output_name:
        Name for the final assembled BSP object (default ``"bsp_world"``).
    sky_height_bu:
        Minimum height of the sky box above ground in Blender units.
    ground_margin_bu:
        Margin added around the campus footprint for the sky envelope walls.
    export_path:
        Path for the output ``.jms`` file.  ``None`` → ``<dir>/naranjos.jms``.
    jms_scale:
        Uniform scale applied to vertex positions in the JMS output.
        The mesh in Blender is NOT modified — scale is applied only during
        writing, equivalent to S→<scale>→Enter in Edit Mode.
        Use ``27.0`` for the Halo CE world-unit convention.
    """
    bsp = assemble_naranjos_level(
        bsp_source_name=bsp_source_name,
        output_name=output_name,
        sky_height_bu=sky_height_bu,
        ground_margin_bu=ground_margin_bu,
    )
    if bsp is None:
        return

    validate_and_report(output_name)
    export_level(obj_name=output_name, output_path=export_path, scale=jms_scale)


